  private void presentarDatos() {
    DAOProducto dao = new DAOProducto();
    
    try{
      this.actual = dao.leer( this.actual);
      if( this.actual != null){
//        this.cboCategoria.setSelectedIndex(
//                this.buscarCategoria( this.actual.getCategoria() )
//          );

        if (this.rbAlimento.isSelected()==true) {
                this.rbAlimento.setSelected(true);
            }else if((this.rbMedicina.isSelected()==true)) {
                this.rbMedicina.setSelected(true);
            }else if((this.rbAccesorio.isSelected()==true)) {
                this.rbAccesorio.setSelected(true);}
                
        this.txtNombre.setText(this.actual.getNombre());
        this.txtPresentacion.setText(this.actual.getPresentacion());
        this.spPrecio.setValue(this.actual.getPrecio());
        this.spCantidad.setValue(this.actual.getCantidad());
        this.spUnidades.setValue(this.actual.getUnidades());
        this.dchFecha.setDate(this.actual.getFecha());
        this.txtDescripcion.setText(this.actual.getDescripcion());
        this.chkVigencia.setSelected(this.actual.isVigencia());
        this.activarControles(true);
      }else{
        JOptionPane.showMessageDialog(this, 
                "No se encontró el producto buscado");
      }
    }catch(Exception e){
      JOptionPane.showMessageDialog(this, e.getMessage());
    }finally{
      dao = null;
    }
  }

