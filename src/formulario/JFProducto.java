    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
      DAOProducto dao;
      ClsProducto prod;
      
      prod = new ClsProducto();
      if (this.rbAlimento.isSelected()==true) {
            prod.setCategoria("Alimento");
        }else if(this.rbMedicina.isSelected()==true) {
            prod.setCategoria("Medicina");
        }else if(this.rbAccesorio.isSelected()==true) {
            prod.setCategoria("Accesorio");
        }
      prod.setNombre(this.txtNombre.getText());
      prod.setPresentacion(this.txtPresentacion.getText());
      prod.setPrecio((double) this.spPrecio.getValue());
      prod.setCantidad((int) this.spCantidad.getValue());
      prod.setUnidades((int) this.spUnidades.getValue());
      prod.setFecha(this.dchFecha.getDate());

      prod.setVigencia(this.chkVigencia.isSelected());
      
//      prod.setCategoria(this.categorias.get(
//              this.cboCategoria.getSelectedIndex()
//              ) );
      
      dao = new DAOProducto();
      try{
        if( this.actual == null){
          dao.registrar(prod);
        }else{
          prod.setCodigo( this.actual .getCodigo());
          dao.actualizar(prod);
        }
        this.activarControles(false);
      }catch(Exception e){
        JOptionPane.showMessageDialog(this, e.getMessage());
      }finally{
        prod = null;
        dao = null;
      }
      
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
      DAOProducto dao = new DAOProducto();

      try {
        this.productos = dao.listar(
                this.txtProducto.getText());
        this.llenarTabla();
      } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
      } finally {
        dao = null;
      }
    }//GEN-LAST:event_btnBuscarActionPerformed

  private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    int pos = this.tblProductos.getSelectedRow();
    
    if( pos > -1){
      this.actual = this.productos.get(pos);
      this.presentarDatos();
    }else{
     JOptionPane.showMessageDialog(this, "Debe seleccionar un producto");
    }    
  }//GEN-LAST:event_btnModificarActionPerformed

