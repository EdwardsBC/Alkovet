    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
                   ClsProveedor prov;
                   DAOProveedor dao;
        if (this.validarDatos()== true) {       
           prov = new ClsProveedor(); 
            
        prov.setRazon_Social(this.txtRazonSocial.getText());
        prov.setRepresentante(this.txtRepresentante.getText());
        prov.setRuc(this.txtRuc.getText());
        prov.setTelefono(this.txtTelefono.getText());
        prov.setCorreo(this.txtCorreo.getText());
        prov.setDireccion(this.txtDireccion.getText());
        prov.setVigencia(this.chkVigencia.isSelected());
    
        dao = new DAOProveedor();
      try{
        if( this.actual == null){
          dao.registrar(prov);
        }else{
          prov.setCodigo( this.actual .getCodigo());
          dao.actualizar(prov);
        }
        this.activarControles(false);
      }catch(Exception e){
        JOptionPane.showMessageDialog(this, e.getMessage());
      }finally{
        prov = null;
        dao = null;
      }
              this.activarControles(false);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

