    private void crearColumnas() {
            this.modelo.addColumn("Razon Social");
            this.modelo.addColumn("Representante");
            this.modelo.addColumn("Ruc");
            this.modelo.addColumn("Telefono");
            this.modelo.addColumn("Correo");
            this.modelo.addColumn("Direccion");
            this.modelo.addColumn("Vigencia");
            
    }

    private boolean validarDatos() {
       boolean validar=true;
           if (txtRazonSocial.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese Razon Social","error", JOptionPane.ERROR_MESSAGE);}
           else if (txtRepresentante.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese Representante","error", JOptionPane.ERROR_MESSAGE);}
           else if (txtRuc.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese RUC","error", JOptionPane.ERROR_MESSAGE);}
           else if (txtTelefono.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese Telefono","error", JOptionPane.ERROR_MESSAGE);}
           else if (txtCorreo.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese Correo","error", JOptionPane.ERROR_MESSAGE);}
           else if (txtDireccion.getText().isEmpty()) {
               JOptionPane.showMessageDialog(null, "Ingrese Direccion","error", JOptionPane.ERROR_MESSAGE);}
        return validar;
    }

    private void llenarTabla() {
        this.modelo.setRowCount(0);
      if (this.proveedores != null) {
         for (ClsProveedor proveedor : proveedores) {
        this.modelo.addRow(new Object[]{
        proveedor.getRazon_Social(),
        proveedor.getRepresentante(),
        proveedor.getRuc(),
        proveedor.getTelefono(),
        proveedor.getCorreo(),
        proveedor.getDireccion(),
        proveedor.isVigencia()
      });
      }
    }
        
    }
