    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        DAOUsuario dao;
    ClsUsuario usuario;
    Veterinaria abr = new Veterinaria();
    if (this.validarDatos() == true) {
      usuario = new ClsUsuario();
      usuario.setNombre(this.txtUsuario.getText());
      usuario.setClave(String.valueOf(this.txtClave.getPassword()));
      
      dao = new DAOUsuario();
      
        Veterinaria frame = new Veterinaria();
       frame.setVisible(true);

      usuario = dao.identificar(usuario);
      
      if (usuario != null) {
        if (usuario.isVigencia() == true) {
          //JOptionPane.showMessageDialog(this, "Bienvenido");
          this.usuario = usuario;
        } else {
          JOptionPane.showMessageDialog(this, "Sus credenciales son correctas pero no tiene permiso");
          this.usuario = null;
        }
        this.setVisible(false);
      } else {
        veces++;
        JOptionPane.showMessageDialog(this,
                "Credenciales no v√°lidas\n"
                + "Le quedan " + (MAX_INTENTOS - veces) + " intentos");
        this.txtUsuario.setText("");
        this.txtClave.setText("");
        this.txtUsuario.requestFocusInWindow();

        if (veces == MAX_INTENTOS) {
          this.setVisible(false);
          this.usuario = null;
        }

      }
    }      
    }//GEN-LAST:event_btnIngresarActionPerformed

