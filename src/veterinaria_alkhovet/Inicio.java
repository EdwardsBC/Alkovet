    private boolean validarDatos() {
       String msje = "";
    boolean correcto = true;

    if (this.txtUsuario.getText().length() == 0) {
      msje += "Debe ingresar el usuario\n";
      correcto = false;
    }
    if (this.txtClave.getPassword().length == 0) {
      msje += "Debe indicar la clave del usuario";
      correcto = false;
    }

    if (correcto == false) {
      JOptionPane.showMessageDialog(this, msje);
    }
    return correcto;
    }
